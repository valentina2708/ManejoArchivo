    private String abrirArchivo() {
       String lineaTexto = "";
       String texto = "";
        try {

            JFileChooser file = new JFileChooser();
            file.showOpenDialog(this);

            File abrir = file.getSelectedFile();
            /**
             * recorremos el archivo, lo leemos para plasmarlo en el area de
             * texto
             */
            if (abrir != null) {
                FileReader archivos=new FileReader(abrir);
                BufferedReader lee = new BufferedReader(archivos);

                while ((lineaTexto = lee.readLine()) != null) {
                
                    
                   // texto += lineaTexto + "\n";
                     System.out.println("" + lineaTexto);
                }

                lee.close();
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex + "/nNO se encontrado el archivo");
        }
        return texto;
    }

